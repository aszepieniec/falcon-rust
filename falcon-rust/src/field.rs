/// q is the integer modulus which is used in Falcon.
pub(crate) const Q: u32 = 12 * 1024 + 1;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) struct Felt(u32);

impl Felt {
    pub const fn new(value: i16) -> Self {
        let z = value >= 0;
        let y = z as i16;
        let x = y - ((!z) as i16);
        let r = x * ((x * value) % (Q as i16));
        let w = (r + (Q as i16) * (1 - y)) as u32;
        Felt((w << 16) % Q)
    }

    pub fn value(&self) -> i16 {
        ((self.0 * 2304) % Q) as i16
    }

    pub fn balanced_value(&self) -> i16 {
        let value = self.value();
        if value > ((Q as i16) / 2) {
            value - (Q as i16)
        } else {
            value
        }
    }

    pub const fn inverse_or_zero(&self) -> Self {
        INV_MOD_Q[self.0 as usize]
    }

    pub fn batch_inverse_or_zero(batch: &[Self]) -> Vec<Self> {
        let mut acc = Felt::one();
        let mut rp: Vec<Felt> = Vec::with_capacity(batch.len());
        for batch_item in batch {
            if !batch_item.is_zero() {
                rp.push(acc);
                acc = *batch_item * acc;
            } else {
                rp.push(Felt::zero());
            }
        }
        let mut inv = acc.inverse_or_zero();
        for i in (0..batch.len()).rev() {
            if !batch[i].is_zero() {
                rp[i] *= inv;
                inv *= batch[i];
            }
        }
        rp
    }

    fn montymul(&self, other: &Felt) -> Felt {
        const Q0I: u32 = 12287;
        let rarb = self.0 * other.0;
        let (rarb_qinv_ofl, _) = rarb.overflowing_mul(Q0I);
        let rab_mod_r = rarb_qinv_ofl & 0xffff;
        let q_rab_mod_r = rab_mod_r * Q;
        let rarb_plus_q_rab_mod_r = rarb + q_rab_mod_r;
        let rarb_plus_q_rab_mod_r_div_r = rarb_plus_q_rab_mod_r >> 16;
        let (z, b) = rarb_plus_q_rab_mod_r_div_r.overflowing_sub(Q);
        let (y, _) = z.overflowing_add(Q * (b as u32));
        Felt(y)
    }
}

#[allow(clippy::suspicious_arithmetic_impl)]
impl Add for Felt {
    fn add(self, rhs: Self) -> Self::Output {
        let (s, _) = self.0.overflowing_add(rhs.0);
        let (d, n) = s.overflowing_sub(Q);
        let (r, _) = d.overflowing_add(Q * (n as u32));
        Felt(r)
    }

    type Output = Self;
}

impl AddAssign for Felt {
    fn add_assign(&mut self, rhs: Self) {
        *self = *self + rhs;
    }
}

impl Sub for Felt {
    type Output = Self;

    fn sub(self, rhs: Self) -> Self::Output {
        self + -rhs
    }
}

impl SubAssign for Felt {
    fn sub_assign(&mut self, rhs: Self) {
        *self = *self - rhs;
    }
}

impl Neg for Felt {
    type Output = Felt;

    fn neg(self) -> Self::Output {
        let is_zero = self.0 == 0;
        let (r, _) = Q.overflowing_sub(self.0);
        Felt(r * (is_zero as u32))
    }
}

impl Mul for Felt {
    fn mul(self, rhs: Self) -> Self::Output {
        self.montymul(&rhs)
    }

    type Output = Self;
}

impl MulAssign for Felt {
    fn mul_assign(&mut self, rhs: Self) {
        *self = *self * rhs;
    }
}

impl Zero for Felt {
    fn zero() -> Self {
        Felt(0)
    }

    fn is_zero(&self) -> bool {
        self.0 == 0
    }
}
impl One for Felt {
    fn one() -> Self {
        Felt(1)
    }
}

impl Distribution<Felt> for Standard {
    fn sample<R: rand::Rng + ?Sized>(&self, rng: &mut R) -> Felt {
        Felt((rng.next_u32() >> 1) % Q)
    }
}

impl Display for Felt {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_fmt(format_args!("{}", self.value()))
    }
}

/// A dictionary of all the roots.
pub(crate) const fn roots_dict_zq(n: usize) -> &'static [Felt] {
    match n {
        2usize => &PHI4_ROOTS_ZQ,
        4usize => &PHI8_ROOTS_ZQ,
        8usize => &PHI16_ROOTS_ZQ,
        16usize => &PHI32_ROOTS_ZQ,
        32usize => &PHI64_ROOTS_ZQ,
        64usize => &PHI128_ROOTS_ZQ,
        128usize => &PHI256_ROOTS_ZQ,
        256usize => &PHI512_ROOTS_ZQ,
        512usize => &PHI1024_ROOTS_ZQ,
        1024usize => &PHI2048_ROOTS_ZQ,
        _ => panic!("n must be a power of two smaller than 2048"),
    }
}

#[allow(clippy::suspicious_arithmetic_impl)]
impl Div for Felt {
    type Output = Felt;

    fn div(self, rhs: Self) -> Self::Output {
        if rhs.is_zero() {
            panic!("Cannot divide by zero");
        } else {
            self * INV_MOD_Q[rhs.0 as usize]
        }
    }
}

