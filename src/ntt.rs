    #[test]
    fn test_ntt_linearity() {
        let mut rng = thread_rng();
        let a: Felt = rng.gen();
        let b: Felt = rng.gen();
        const N: usize = 256;
        let v: [Felt; N] = (0..N).map(|_| rng.gen()).collect_vec().try_into().unwrap();
        let w: [Felt; N] = (0..N).map(|_| rng.gen()).collect_vec().try_into().unwrap();

        let v_ntt = ntt(&v);
        let w_ntt = ntt(&w);
        let lhs = v_ntt
            .into_iter()
            .zip(w_ntt.into_iter())
            .map(|(vv, ww)| a * vv + b * ww)
            .collect_vec();

        let rhs_coeffs = v
            .into_iter()
            .zip(w.into_iter())
            .map(|(vv, ww)| a * vv + b * ww)
            .collect_vec();
        let rhs = ntt(&rhs_coeffs);

        assert_eq!(lhs, rhs);
    }

